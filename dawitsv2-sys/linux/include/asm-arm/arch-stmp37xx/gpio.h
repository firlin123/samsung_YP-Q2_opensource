static inline void __stmp37xx_config_pin(unsigned int pin)
{
	unsigned int p = ext_GPIO_PIN(pin);
	unsigned int mux = ext_GPIO_MUX(pin);
	unsigned int sel = ext_GPIO_SEL(pin);
	unsigned int bank = ext_GPIO_BANK(pin);
	unsigned int vol = ext_GPIO_VOL(pin);
	unsigned int ma = ext_GPIO_MA(pin);
	unsigned int dir = ext_GPIO_DIR(pin);
	unsigned int af = ext_GPIO_AF(pin);
	unsigned int pu = ext_GPIO_PU(pin);
	unsigned int msk, shift, val, dif;

	/* drive current, voltage */
	shift = (p % 8) * 4;
	msk = 0x7 << shift;
	val = ((vol << 2) | (ma)) << shift;
	dif = (REG_CTL_DRV(sel) & msk) ^ val;
	REG_CTL_DRV_TOG(sel) = dif;

	if (af == GPIO_AF3) {
		/* direction */
		shift = p;
		msk = 0x1 << shift;
		val = dir << shift;
		if (dir)
			REG_CTL_DOE_SET(bank) = val;
		else
			REG_CTL_DOE_CLR(bank) = msk;
	}


	/* muxsel */
	shift = (p % 16) * 2;
	msk = 0x3 << shift;
	val = af << shift;
	dif = (REG_MUX_SEL(mux) & msk) ^ val;
	REG_MUX_SEL_TOG(mux) = dif;

	/* pullup */
	shift = p;
	msk = 0x1 << shift;
	val = pu << shift;
	if (pu)
		REG_CTL_PULL_SET(bank) = val;
	else
		REG_CTL_PULL_CLR(bank) = msk;

	/* done */
}

static inline void __stmp37xx_gpio_set_af(unsigned int pin, int function)
{
	unsigned int p = (ext_GPIO_PIN(pin) % 16) * 2;
	unsigned int mux = ext_GPIO_MUX(pin);
	unsigned int msk = 0x3 << p;
	unsigned int val = (function & 0x3) << p;
	unsigned int dif;

	dif = (REG_MUX_SEL(mux) & msk) ^ val;
	REG_MUX_SEL_TOG(mux) = dif;
}

static inline int __stmp37xx_gpio_get_af(unsigned int pin)
{
	unsigned int p = (ext_GPIO_PIN(pin) % 16) * 2;
	unsigned int mux = ext_GPIO_MUX(pin);
	unsigned int val;

	val = (REG_MUX_SEL(mux) >> p) & 0x3;
	return (int)val;
}

static inline void __stmp37xx_gpio_set_dir(unsigned int pin, int direction)
{
	unsigned int p = ext_GPIO_PIN(pin);
	unsigned int bank = ext_GPIO_BANK(pin);
	unsigned int msk = 0x1 << p;
	if (direction == GPIO_DIR_OUT)
		REG_CTL_DOE_SET(bank) = msk;
	else
		REG_CTL_DOE_CLR(bank) = msk;
}

static inline int __stmp37xx_gpio_get_dir(unsigned int pin)
{
	unsigned int p = ext_GPIO_PIN(pin);
	unsigned int bank = ext_GPIO_BANK(pin);
	unsigned int msk = 0x1 << p;

	if (REG_CTL_DOE(bank) & msk)
		return GPIO_DIR_OUT;

	return GPIO_DIR_IN;
}

