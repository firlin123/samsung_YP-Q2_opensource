static inline void __stmp37xx_gpio_set_drv(unsigned int pin, int drive)
{
	unsigned int p = (ext_GPIO_PIN(pin) % 8) * 4;
	unsigned int sel = ext_GPIO_SEL(pin);
	unsigned int msk = 0x3 << p;
	unsigned int val = (drive & 0x3) << p;
	unsigned int dif;

	dif = (REG_CTL_DRV(sel) & msk) ^ val;
	REG_CTL_DRV_TOG(sel) = dif;
}

static inline int __stmp37xx_gpio_get_drv(unsigned int pin)
{
	unsigned int p = (ext_GPIO_PIN(pin) % 8) * 4;
	unsigned int sel = ext_GPIO_SEL(pin);
	unsigned int msk = 0x3 << p;
	unsigned int val;

	val = (REG_CTL_DRV(sel) & msk) >> p;
	return (int)val;
}

